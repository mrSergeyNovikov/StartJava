public class Variable {

	public static void main(String[] args) {
		byte kernel = 8;
		short windowsSystem = 10;
		int assembl = 18362;
		long timeToUse = 12960000;
		float physicalMemory = 6.90f;
		double virtualMemory = 9.70d;
		char сomputerСopyright = 'C';
		boolean firewall = true;

		System.out.println("Количество ядер:" + kernel);
		System.out.println("Операционная система: " + windowsSystem);
		System.out.println("Сборка операционной системы Windows: " + assembl);
		System.out.println("Время использования компьютера(в секундах): " + timeToUse);
		System.out.println("Доступная физическая память: " + physicalMemory);
		System.out.println("Доступная виртуальная память: " + virtualMemory);
		System.out.println("Компьтер защищен лицензией: " + сomputerСopyright);
		System.out.println("Включенный firefall: " + firewall);
	}
	
}